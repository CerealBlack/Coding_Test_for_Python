# 정수 n을 입력받기 
n = int(input()) 

# 앞서 계산된 결과를 저장하기 위한 DP 테이블 초기화 
d = [0] * 1001 # 1 <= n <= 1000 이니까 

# 다이나믹 프로그래밍 진행(보텀업) 
# 세로 2, 가로 N인 2 X N 크기를 가지고 있으니까 
d[1] = 1 # 첫번째일때 2 X 1을 가지고 채울 수 있는 경우의 수는 1가지
d[2] = 3 # 두번째일때 2 X 2를 채울 수 있는 경우의 수는 3가지가 된다.
# d[2]까지 미리 작성을 하는 이유는 점화식이 i-2까지 필요하고 3부터 시작하기 때문이다.

# 점화식을 따라 3에서 n까지 반복문 실행 
for i in range(3, n + 1):
    d[i] = (d[i - 1] + 2 * d[i - 2]) % 796796

# 결과 출력
print(d[n]) 
